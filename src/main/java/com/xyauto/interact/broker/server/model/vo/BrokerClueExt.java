package com.xyauto.interact.broker.server.model.vo;

import java.util.ArrayList;
import java.util.List;

public class BrokerClueExt {

    private String mobile;
    private List<BrokerClue> brokerClueList;
    public BrokerClueExt(){

    }
    public BrokerClueExt(String mobile, BrokerClue t){
        this.mobile =mobile;
        this.brokerClueList = new ArrayList<BrokerClue>();
        this.brokerClueList.add(t);
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public List<BrokerClue> getBrokerClueList() {
        return brokerClueList;
    }

    public void setBrokerClueList(List<BrokerClue> brokerClueList) {
        this.brokerClueList = brokerClueList;
    }
}
