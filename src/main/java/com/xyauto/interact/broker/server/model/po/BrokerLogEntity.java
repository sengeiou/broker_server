package com.xyauto.interact.broker.server.model.po;

public class BrokerLogEntity {
    
    private String type;
    
    private long brokerId;
    
    private long dealerId;
    
    private long targetId;
    
    private long createTime;
    
    private String message;
    
    private Object extra;
    
    public BrokerLogEntity(String type, long brokerId, long dealerId, long targetId, String message, Object extra){
        this.type = type;
        this.brokerId = brokerId;
        this.dealerId = dealerId;
        this.targetId = targetId;
        this.message = message;
        this.extra = extra;
    }
    
    public BrokerLogEntity() {}

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the brokerId
     */
    public long getBrokerId() {
        return brokerId;
    }

    /**
     * @param brokerId the brokerId to set
     */
    public void setBrokerId(long brokerId) {
        this.brokerId = brokerId;
    }

    /**
     * @return the targetId
     */
    public long getTargetId() {
        return targetId;
    }

    /**
     * @param targetId the targetId to set
     */
    public void setTargetId(long targetId) {
        this.targetId = targetId;
    }

    /**
     * @return the extra
     */
    public Object getExtra() {
        return extra;
    }

    /**
     * @param extra the extra to set
     */
    public void setExtra(Object extra) {
        this.extra = extra;
    }

    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }

    /**
     * @return the dealerId
     */
    public long getDealerId() {
        return dealerId;
    }

    /**
     * @param dealerId the dealerId to set
     */
    public void setDealerId(long dealerId) {
        this.dealerId = dealerId;
    }
    
    /**
     * @return the createTime
     */
    public long getCreateTime() {
        return createTime;
    }

    /**
     * @param createTime the createTime to set
     */
    public void setCreateTime(long createTime) {
        this.createTime = createTime;
    }
    
}
