package com.xyauto.interact.broker.server.enums;

public enum BrokerNoticeSettingEnum {
    ClueSetting("16", "线索消息"),
    TalkSetting("8", "微聊消息"),
    QASetting("4", "问答消息"),
    DealSetting("2", "业绩消息"),
    TaskSetting("1", "任务消息");


    private String name;
    private String desc;

    private BrokerNoticeSettingEnum(String name, String value) {
        this.name = name;
        this.desc = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
