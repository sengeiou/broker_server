package com.xyauto.interact.broker.server.enums;

public enum  BrokerTemplateTypeEnum {
    //1短信，2微聊 3 微店标题 4 微店导语
    SMSMessage("短信",(short) 1),
    TalkMessage("微聊",(short) 2),
    StoreTitle("微店标题",(short) 3),
    StoreContent("微店导语",(short) 4);

    BrokerTemplateTypeEnum(String name,short value) {
        this.name = name;
        this.value = value;
    }

    private String name;
    private short value;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public short getValue() {
        return value;
    }

    public void setValue(short value) {
        this.value = value;
    }
}
