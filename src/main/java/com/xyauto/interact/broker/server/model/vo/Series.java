package com.xyauto.interact.broker.server.model.vo;

import java.util.Date;
import org.apache.commons.lang.StringUtils;

public class Series {

    /**
     * 品牌id
     */
    private int brandId;

    /**
     * 车系id
     */
    private int seriesId;

    /**
     * 子品牌id
     */
    private int subBrandId;

    /**
     * 车系名称
     */
    private String name = StringUtils.EMPTY;

    /**
     * 车系标识
     */
    private String urlspell = StringUtils.EMPTY;

    /**
     * 车系白底图
     */
    private String image;

    /**
     * 创建时间
     */
    private Date createTime;

    /**
     * @return the brandId
     */
    public int getBrandId() {
        return brandId;
    }

    /**
     * @param brandId the brandId to set
     */
    public void setBrandId(int brandId) {
        this.brandId = brandId;
    }

    /**
     * @return the seriesId
     */
    public int getSeriesId() {
        return seriesId;
    }

    /**
     * @param seriesId the seriesId to set
     */
    public void setSeriesId(int seriesId) {
        this.seriesId = seriesId;
    }

    /**
     * @return the subBrandId
     */
    public int getSubBrandId() {
        return subBrandId;
    }

    /**
     * @param subBrandId the subBrandId to set
     */
    public void setSubBrandId(int subBrandId) {
        this.subBrandId = subBrandId;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the urlspell
     */
    public String getUrlspell() {
        return urlspell;
    }

    /**
     * @param urlspell the urlspell to set
     */
    public void setUrlspell(String urlspell) {
        this.urlspell = urlspell;
    }

    /**
     * @return the image
     */
    public String getImage() {
        return image;
    }

    /**
     * @param image the image to set
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     * @return the createTime
     */
    public Date getCreateTime() {
        return createTime;
    }

    /**
     * @param createTime the createTime to set
     */
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

}
